def _haversine_km(lat1, lon1, lat2, lon2):
    R = 6371
    dlat = math.radians(lat2 - lat1)
    dlon = math.radians(lon2 - lon1)
    a = math.sin(dlat/2)**2 + math.cos(math.radians(lat1)) * math.cos(math.radians(lat2)) * math.sin(dlon/2)**2
    return R * 2 * math.asin(math.sqrt(a))


def _nearest_neighborhood(lat, lon):
    """Return the name of the closest neighborhood to a lat/lon point."""
    best_name, best_dist = None, float("inf")
    for name, info in NEIGHBORHOODS.items():
        d = _haversine_km(lat, lon, info["lat"], info["lon"])
        if d < best_dist:
            best_dist = d
            best_name = name
    return best_name, best_dist


def _load_monitor_locations():
    """Load unique AQS monitor locations from cached CSV if available."""
    path = os.path.join(DATA_DIR, "aqs_monitors.csv")
    if not os.path.exists(path):
        return []
    df = pd.read_csv(path)
    if "latitude" not in df.columns or "longitude" not in df.columns:
        return []
    unique = df.drop_duplicates(subset=["latitude", "longitude"])[["latitude", "longitude"]]
    monitors = []
    for _, row in unique.iterrows():
        name, dist = _nearest_neighborhood(row["latitude"], row["longitude"])
        monitors.append({
            "lat": row["latitude"],
            "lon": row["longitude"],
            "nearest": name,
            "dist_km": round(dist, 1),
        })
    return monitors


@app.route("/study-map")
def study_map():
    """Map showing study neighborhoods and AQS monitor locations."""
    m = folium.Map(location=[39.28, -76.60], zoom_start=10, tiles="CartoDB positron")

    colors = {
        "both": "#ef4444",
        "i95": "#f59e0b",
        "control": "#6b7280",
    }

    for name, info in NEIGHBORHOODS.items():
        if info["near_wheelabrator"] and info["near_i95"]:
            group = "both"
            tag = "Near Wheelabrator & I-95"
        elif info["near_i95"]:
            group = "i95"
            tag = "I-95 Corridor Only"
        else:
            group = "control"
            tag = "Control Area"

        folium.CircleMarker(
            location=[info["lat"], info["lon"]],
            radius=9,
            color=colors[group],
            fill=True,
            fill_color=colors[group],
            fill_opacity=0.7,
            popup=f"<b>{name}</b><br>{tag}",
            tooltip=name,
        ).add_to(m)

    # Wheelabrator marker
    folium.Marker(
        location=[WHEELABRATOR["lat"], WHEELABRATOR["lon"]],
        popup=f"<b>{WHEELABRATOR['name']}</b><br>{WHEELABRATOR['address']}",
        tooltip="Wheelabrator Baltimore",
        icon=folium.Icon(color="darkred", icon="industry", prefix="fa"),
    ).add_to(m)

    # AQS monitor locations (blue)
    aqs_monitors = _load_monitor_locations()
    for mon in aqs_monitors:
        folium.CircleMarker(
            location=[mon["lat"], mon["lon"]],
            radius=7,
            color="#3b82f6",
            fill=True,
            fill_color="#3b82f6",
            fill_opacity=0.8,
            popup=f"<b>EPA AQS Monitor</b><br>{mon['lat']:.4f}, {mon['lon']:.4f}<br>Nearest: {mon['nearest']} ({mon['dist_km']} km)",
            tooltip=f"AQS Monitor (near {mon['nearest']})",
        ).add_to(m)

    # Legend
    legend = """
    <div style="position:fixed; bottom:30px; left:30px; z-index:1000;
                background:white; padding:10px 14px; border-radius:8px;
                border:1px solid #ccc; font:13px/1.6 sans-serif;">
        <b>Study Areas</b><br>
        <i class="fa fa-industry" style="color:#8b0000"></i> Wheelabrator<br>
        <span style="color:#ef4444">&#9679;</span> Near Both (WB + I-95)<br>
        <span style="color:#f59e0b">&#9679;</span> I-95 Corridor Only<br>
        <span style="color:#6b7280">&#9679;</span> Control<br>
        <span style="color:#3b82f6">&#9679;</span> EPA AQS Monitor
    </div>
    """
    m.get_root().html.add_child(folium.Element(legend))

    return Response(m._repr_html_(), mimetype="text/html")


