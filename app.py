def discover_monitor_charts():
    """Scan output/ for per-monitor HTML files generated by the analysis pipeline."""
    monitors = {}
    for path in glob.glob(os.path.join(OUTPUT_DIR, "pollution_rose_*.html")):
        basename = os.path.basename(path)
        # Extract the site label, e.g. "39.29_-76.47" from "pollution_rose_39.29_-76.47.html"
        label = basename.replace("pollution_rose_", "").replace(".html", "")
        monitors[label] = {"label": label}

    # Attach whichever related files exist for each monitor
    for label in monitors:
        monitors[label]["pollution_rose"] = f"pollution_rose_{label}.html"
        for chart_type in ["directional", "timeseries", "seasonal"]:
            fname = f"{chart_type}_{label}.html"
            if os.path.exists(os.path.join(OUTPUT_DIR, fname)):
                monitors[label][chart_type] = fname

    return monitors


@app.route("/")
def index():
    """Dashboard landing page with all visualizations."""
    emissions_df = get_emissions_df()
    ghg_df = get_ghg_df()

    e2017 = {r["pollutant"]: r for _, r in emissions_df[emissions_df["year"] == 2017].iterrows()}
    e2014 = {r["pollutant"]: r for _, r in emissions_df[emissions_df["year"] == 2014].iterrows()}

    monitors = discover_monitor_charts()

    context = {
        "facility": WHEELABRATOR,
        "neighborhoods": NEIGHBORHOODS,
        "emissions_2017": e2017,
        "emissions_2014": e2014,
        "ghg_2017": ghg_df[ghg_df["year"] == 2017].iloc[0]["co2e_metric_tons"],
        "total_lbs_2017": sum(EMISSIONS_DATA[2017].values()),
        "total_lbs_2014": sum(EMISSIONS_DATA[2014].values()),
        "monitors": monitors,
    }
    return render_template("index.html", **context)


@app.route("/presentation")
def presentation():
    """Presentation-style page with copy-ready text and images."""
    context = get_presentation_context()
    chart_lookup = discover_monitor_charts()
    interactive_monitors = []
    for row in context.get("monitor_summary", []):
        raw = chart_lookup.get(row["chart_label"], {})
        charts = {}
        for key, value in raw.items():
            if key == "label":
                charts[key] = value
            else:
                charts[key] = f"/output/{value}"
        interactive_monitors.append({**row, "charts": charts})
    context["interactive_monitors"] = interactive_monitors
    return render_template("presentation.html", **context)


